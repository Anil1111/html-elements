let setActiveStyleSheet = function (title) {
    [].forEach.call(
        document.querySelectorAll('link'),
        function (el) {
          if (el.getAttribute('rel').indexOf('style') != -1 && el.getAttribute('title')) {
            el.disabled = true
            if (el.getAttribute('title') == title) {
              el.disabled = false
            }
          }
        }
    )
}

let toolbar = document.createElement('div')
toolbar.id = 'toolbar'
toolbar.innerHTML = `
  <label for="switch-css">Switch CSS</label>
  <select id="switch-css">
      <option value="">User Agent Style Sheet</option>
  </select>
  <a href="https://github.com/kemar/html-elements">GitHub</a>`
document.querySelector('body').prepend(toolbar)

let switchElement = document.querySelector('#switch-css')

Object.keys(styles).forEach(function (key) {

    // Create <optgroup>.
    let optgroup = document.createElement('optgroup')
    optgroup.label = key

    styles[key].forEach(function (item) {

        // Add <link> elements in the <head> section.
        let link = document.createElement('link')
        link.rel = 'alternate stylesheet'
        link.href = item.href
        link.title = item.name
        document.head.appendChild(link)

        // Add <option>s to <optgroup>.
        let option = document.createElement('option')
        option.value = item.name
        option.text = item.name.replace('-', ' ')
        optgroup.appendChild(option)

    })

    // Add <optgroup> to <select>.
    switchElement.appendChild(optgroup)

})

switchElement.addEventListener('change', function (e) {
    setActiveStyleSheet(e.target.value)
})

let body = document.querySelector('body')
body.style.paddingTop = document.querySelector('#toolbar').offsetHeight + 'px'
